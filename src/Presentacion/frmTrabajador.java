    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //valido los campos:
        btnGuardar.transferFocus();
        if (txtNombre.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre para el Trabajador.");
            txtApaterno.requestFocus();
            return;

        }
        
        if (txtApaterno.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar unApellido Paterno para el Trabajador.");
            txtApaterno.requestFocus();
            return;

        }
        
        if (txtAmaterno.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Apellido Materno para el Trabajador.");
            txtApaterno.requestFocus();
            return;

        }

        if (txtDireccion.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una DirecciÃ³n para el Trabajador.");
            txtDireccion.requestFocus();
            return;

        }

        if (txtNumeroDocumento.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un NÃºmero Documento para la venta del Producto.");
            txtNumeroDocumento.requestFocus();
            return;

        }
        if (txtSueldo.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Sueldo para el Trabajador.");
            txtSueldo.requestFocus();
            return;

        }
        
        if (txtPassword.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Password para el Trabajador.");
            txtPassword.requestFocus();
            return;

        }
        
        if (txtLogin.getText().length() == 0)
        {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Login para el Trabajador.");
            txtLogin.requestFocus();
            return;

        }

        VTrabajador datos  = new VTrabajador();
        FTrabajador  funcion = new FTrabajador();
        
        datos.setNombre(txtNombre.getText());
        datos.setApaterno(txtAmaterno.getText());
        datos.setAmaterno(txtApaterno.getText());

        int seleccionado = cboAcceso.getSelectedIndex();
        datos.setTipo_documento((String)cboAcceso.getItemAt(seleccionado));

        datos.setNum_documento(txtNumeroDocumento.getText());

        datos.setDireccion(txtDireccion.getText());
        datos.setTelefono(txtTelefono.getText());
        datos.setEmail(txtEmail.getText());
        
        datos.setSueldo(Double.parseDouble(txtSueldo.getText()));
        seleccionado = cboAcceso.getSelectedIndex();
        datos.setAcceso((String)cboAcceso.getItemAt(seleccionado));
        datos.setLogin(txtLogin.getText());
        datos.setPassword(txtPassword.getText());
        seleccionado = cboEstado.getSelectedIndex();
        datos.setEstado((String)cboEstado.getItemAt(seleccionado));
        
        

        if (accion.equals("guardar"))
        {
            if (funcion.Insertar(datos))
            {
                JOptionPane.showConfirmDialog(rootPane, "El Trabajador fue registrada satisfactoriamente.");
                Mostrar("");
                Inhabilitar();
            }
        }
        else if(accion.equals("editar"))
        {
            datos.setIdpersona(Integer.parseInt(txtIdTrabajador.getText()));

            if (funcion.Editar(datos))
            {
                JOptionPane.showConfirmDialog(rootPane, "El Trabajador fue Editado satisfatoriamente.");
                Mostrar("");
                Inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtIdTrabajador.setText(tablalistado.getValueAt(fila, 0).toString());
        txtNombre.setText(tablalistado.getValueAt(fila, 1).toString());
        txtAmaterno.setText(tablalistado.getValueAt(fila, 2).toString());
        txtApaterno.setText(tablalistado.getValueAt(fila, 3).toString());
        cbotipoDocumento1.setSelectedItem(tablalistado.getValueAt(fila, 4));
        txtNumeroDocumento.setText(tablalistado.getValueAt(fila, 5).toString());        
        txtDireccion.setText(tablalistado.getValueAt(fila, 6).toString());
        txtTelefono.setText(tablalistado.getValueAt(fila, 7).toString());
        txtEmail.setText(tablalistado.getValueAt(fila, 8).toString());
        
        txtSueldo.setText(tablalistado.getValueAt(fila, 9).toString());
        cboAcceso.setSelectedItem(tablalistado.getValueAt(fila, 10).toString());
        txtLogin.setText(tablalistado.getValueAt(fila, 11).toString());
        txtPassword.setText(tablalistado.getValueAt(fila, 12).toString());
        cboEstado.setSelectedItem(tablalistado.getValueAt(fila, 13).toString());
        

    }//GEN-LAST:event_tablalistadoMouseClicked

