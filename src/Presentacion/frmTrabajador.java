    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //valido texto:
        if (!txtIdTrabajador.getText().equals(""))
        {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "EstÃ¡s seguro de Eliminar el Cliente?", "Confirmar", 2);

            if (confirmacion == 0)
            {
                FTrabajador funcion = new FTrabajador();
                VTrabajador datos = new VTrabajador();

                datos.setIdpersona(Integer.parseInt(txtIdTrabajador.getText()));
                funcion.Eliminar(datos);
                Mostrar("");
                Inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

