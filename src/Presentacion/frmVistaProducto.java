    private void tablalistadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMousePressed
        
        if (evt.getClickCount() == 2) 
        {
            int fila =  tablalistado.getSelectedRow();
            
            String IdProducto, Producto, precioVenta;
            
            IdProducto = tablalistado.getValueAt(fila, 0).toString();
            Producto = tablalistado.getValueAt(fila, 1).toString();
            precioVenta = tablalistado.getValueAt(fila, 4).toString();
            
            frmConsumo.txtidproducto.setText(IdProducto);
            frmConsumo.txtProducto.setText(Producto);
            frmConsumo.txtPrecioVenta.setText(precioVenta);
            
            this.dispose();
        }
    }//GEN-LAST:event_tablalistadoMousePressed

