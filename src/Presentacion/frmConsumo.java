    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //valido texto:
        if (!txtidconsumo.getText().equals(""))
        {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Est√°s seguro de Eliminar el Producto?", "Confirmar", 2);

            if (confirmacion == 0)
            {
                Fconsumo funcion = new Fconsumo();
                VConsumo datos = new VConsumo();

                datos.setIdconsusmo(Integer.parseInt(txtidconsumo.getText()));
                funcion.Eliminar(datos);
                Mostrar(idreserva);
                Inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

